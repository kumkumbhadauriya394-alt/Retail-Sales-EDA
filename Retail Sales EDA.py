import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# Load Clean Dataset
data = pd.read_csv("Retail_Sales_Cleaned.csv")
print(data.head(5))


'Step 1: Dataset Overview (KPI Analysis)'

print("\n" + "="*50)
print("RETAIL SALES DATASET OVERVIEW")
print("="*50)

print(f"\nTotal Orders      : {data['Order_ID'].nunique()}")
print(f"Total Customers   : {data['Customer_ID'].nunique()}")
print(f"Total Sales       : ₹{data['Total_Sales'].sum():,.2f}")
print(f"Average Order     : ₹{data['Total_Sales'].mean():,.2f}")
print(f"Average Rating    : {data['Rating'].mean():.2f}\n")



'Category-wise Sales'

print("\n" + "="*60)
print("Category-wise Sales")
print("="*60)

category_sales = data.groupby("Category")["Total_Sales"].sum()

print(category_sales)

'1 Chart (Bar Chart)'

category_sales = data.groupby("Category")["Total_Sales"].sum().sort_values()

plt.figure(figsize=(12, 6))

plt.bar(category_sales.index, category_sales.values)

plt.title("Category Wise Sales", fontsize=15, fontweight="bold")
plt.xlabel("Category")
plt.ylabel("Total Sales (₹)")

plt.xticks(rotation=45)

plt.tight_layout()
plt.savefig("Images/category_sales.png", dpi=300)
plt.show()

print("\n📊 Analysis:")
print("Category-wise total sales were calculated to identify the best-performing product categories.")

print("\n👀 Observation:")
print("Fashion recorded the highest sales (₹2.17M).")
print("Electronics was the second highest.")
print("Home generated the lowest sales.")

print("\n💡 Business Insight:")
print("Focus more on Fashion and Electronics, while improving marketing strategies for the Home category.")




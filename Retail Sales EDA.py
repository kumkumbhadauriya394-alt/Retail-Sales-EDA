'Correlation Heatmap'
numeric_data = data[[
    "Age",
    "Quantity",
    "Price",
    "Discount",
    "Total_Sales",
    "Rating"
]]

# Create Correlation Matrix

correlation = numeric_data.corr()

print(correlation)

'8 Chart (correlation Heatmap)'

plt.figure(figsize=(12,6))

sns.heatmap(
    correlation,
    annot=True,
    fmt=".2f",
    linewidths=0.5
)

plt.title("Correlation Heatmap", fontsize=16, fontweight="bold")

plt.tight_layout()
plt.savefig("Images/correlation_heatmap.png", dpi=300)
plt.show()

print("\n📊 Analysis:")
print("This heatmap shows the strength and direction of relationships between the numerical variables in the dataset.")

print("\n👀 Observation:")
print("Price has the strongest positive correlation with Total_Sales (0.69).")
print("Quantity also has a strong positive correlation with Total_Sales (0.61).")
print("Discount has a weak negative correlation with Total_Sales (-0.12).")
print("Age and Rating have very weak correlations with the other variables.")

print("\n💡 Business Insight:")
print("Variables with a strong positive correlation can be used to improve sales forecasting.")
print("Weak correlations indicate that other factors may influence customer behavior and should be explored further.")




'Monthly Sales Trend'

print("\n" + "="*60)
print("Monthly Sales Trend")
print("="*60)

data["Order_Date"] = pd.to_datetime(data["Order_Date"])
data["Month"] =  data["Order_Date"].dt.month_name()


month_order = [
    "January", "February", "March", "April", "May", "June",
    "July", "August", "September", "October", "November", "December"
]

monthly_sales = (
    data.groupby("Month")["Total_Sales"]
    .sum()
    .reindex(month_order)
)

print(monthly_sales)

'4 Chart (Line Chart)'

plt.figure(figsize=(12, 6))

plt.plot(
    monthly_sales.index,
    monthly_sales.values,
    marker='o',
    linestyle='-',
    linewidth=2.5,
    color='royalblue'
)

plt.title("Monthly Sales Trend", fontsize=16, fontweight='bold')
plt.xlabel("Month", fontsize=12)
plt.ylabel("Total Sales", fontsize=12)

plt.xticks(rotation=45)
plt.grid(True, linestyle='--', alpha=0.5)

plt.tight_layout()
plt.savefig("Images/monthly_sales.png", dpi=300)
plt.show()

print("\n📊 Analysis:")
print("This analysis shows how total sales change from month to month and helps identify sales trends throughout the year.")

print("\n👀 Observation:")
print("Sales peaked in May.")
print("Lowest sales were recorded in September.")

print("\n💡 Business Insight:")
print("Focus marketing during low-sales months.")
print("Maintain sufficient stock for high-sales months.")



